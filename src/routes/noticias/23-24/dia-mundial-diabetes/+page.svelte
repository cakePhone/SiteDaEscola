      forma de prevenção, pelas alunas do 11ºAS e confecionado um batido de frutos vermelhos, dinamizado no âmbito dos
      projetos múltiplos da PAP da aluna, Ema do 12ºAS.
    </p>
    <p>
      Esta atividade foi dinamizada pela equipa de enfermeiros: Soraia Ferreira, Sónia Pereira e Carlos Moura, com a
      colaboração da formadora, Clarisse Lemos e do professor, Rui Taboada, Coordenador do Projeto, "Eco Escolas".
