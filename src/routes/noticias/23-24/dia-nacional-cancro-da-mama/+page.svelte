      Esta atividade foi implementada, com a ajuda preciosa, da coordenadora da Educação para a Saúde da Escola pois
      permitiu-nos articular com a Equipa de Saúde Escolar, que cedeu panfletos, e com a Liga Nacional da Luta Contra o
      Cancro, que nos permitiu acesso a vídeos sobre a temática que estiveram em visualização na sala dos professores e
      na sala de convívio dos alunos.
