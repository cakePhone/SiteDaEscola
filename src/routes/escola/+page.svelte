    <h2 id="historia">História</h2>
    <h3 id="raizes">Raízes</h3>
    <p>
      A Escola Secundária de Amarante vai buscar as suas raízes à Escola Industrial de Amarante, criada em 1964. Após
      esforços e diligências de alguns cidadãos e entidades amarantinas, entre as quais é justo destacar as Câmaras da
      presidência do Coronel Carvalho Lima e de Adriano Soares Natal, foi publicado no Diário do Governo o Decreto-Lei
      45980, de 20/10/64, que cria a Escola Industrial de Amarante. Era então Ministro da Educação o professor Galvão
      Teles.
    </p>
    <p>
      Poucos dias depois, mais precisamente a 28 de outubro, foi realizada uma reunião ordinária da Câmara Municipal,
      tendo na ordem de trabalhos a Escola Industrial de Amarante, o que evidencia o empenho da autarquia na rápida
      entrada em funcionamento da nova Escola, correspondendo aos legítimos anseios de uma comunidade até então
      arredada
      do acesso à educação e ensino, para além do nível primário, por falta de resposta local e de recursos
      económicos.
    </p>
    <p>
      A Escola iniciou funções no dia 4 de dezembro de 1964 nas instalações da própria Câmara, sitas no Convento de S.
      Gonçalo, onde funcionou durante os anos letivos de 1964/65 e 1965/66, mantendo-se ainda aí em funcionamento a
      partir dessa data, e até 1968, algumas disciplinas de caráter técnico.
    </p>
    <p>
      Foi designado como seu primeiro Diretor e instalador o Dr. António Baptista Martins, destacado da Escola Brotero
      de Coimbra, onde era professor efetivo, abrindo a Escola helvetica com a oferta limitada ao Ciclo Preparatório.
      Dois anos depois deu-se a abertura do Curso Geral de Formação Feminina e do Curso de Formação Eletromecânica,
      sendo estes ministrados por um total de 6 professores. Os serviços administrativos foram assegurados, no período
      de abertura, por um funcionário da Escola de Penafiel, apoiado por dois elementos da Câmara de Amarante, que
      exerciam funções de pessoal auxiliar.
    </p>
    <p>
      Passados os dois anos iniciais a Escola instalou-se na Casa de Roçadas, na qual a Câmara Municipal havia
      efetuado
      obras de remodelação para o efeito, e, mais tarde, porque estas novas instalações se vieram a revelar também
      insuficientes, instalou-se, em simultâneo, no Arquinho, no edifício do antigo Colégio de S. Gonçalo, que
      entretanto se havia mudado para novas instalações.
    </p>
    <p>
      De 1971 a 1974 funcionou, no edifício do Arquinho, uma Secção Liceal integrada na Escola, mas dependendo do
      Liceu
      de Guimarães e, no ano seguinte, como resultado das políticas educativas do Portugal democrático, pós 25 de
      abril,
      que acabaram com a distinção entre Escolas Técnicas e Liceus, a então Escola Industrial de Amarante é
      transformada
      na atual Escola Secundária de Amarante, conforme Decreto Lei 260-B/75, de 26 de Maio, nela passando a funcionar
      os
      cursos gerais unificados dos ensinos liceal e técnico.
    </p>
    <h3 id="pos25">Pós 25 de Abril</h3>
    <p>
      Em 1975/76 , a Escola Secundária passou também a funcionar na Casa da Pedreira, atualmente designada Casa da
      Portela, devido a um crescimento que, tendo-se mantido nos anos seguintes, levou à construção de uma nova
      Escola,
      localizada nos terrenos do campo de jogos do Amarante Futebol Clube e áreas circundantes entretanto adquiridos
      pela Câmara Municipal, agora sob a presidência do Dr Amadeu Cerqueira da Silva.
    </p>
    <h3 id="nova-escola">Uma nova escola</h3>
    <p>
      A nova Escola, primeira construída de raiz, entrou em funcionamento em 1982, mantendo-se ainda hoje no mesmo
      local, ainda que não possa dizer-se que é a mesma Escola. Na verdade, mercê das obras de remodelação a que vem
      sendo sujeita sob a responsabilidade da Parque Escolar, e que, na presente data, se encontram em fase de
      conclusão, pode dizer-se que a Escola de 82 está já transformada num campus escolar de excelência,
      proporcionando
      bem estar e condições de trabalho excecionais e que merecem os mais rasgados elogios de toda a comunidade e
      enchem
      de satisfação e orgulho os seus alunos, professores e funcionários.
    </p>
    <p>
      Somos, assim, hoje, uma Escola moderna, atrativa e de grande qualidade, com uma oferta educativa e formativa que
      abrange o 3º ciclo do ensino básico, todos os cursos cientifico-humanísticos do ensino secundário e o ensino
      secundário profissional, que é sede de um Centro de Formação de Associação de Escolas - o Centro de Formação de
      Associação de Escolas de Amarante e Baião - e de um Centro para a Qualificação e Ensino Profissional (CQEP) que
      abrange os concelhos de Amarante, Baião e Mesão Frio.
    </p>
    <p>
      A nossa <b>Visão</b> é sermos uma escola de referência pela formação cívica e sucesso académico e profissional
      dos
      nossos
      alunos, pela satisfação dos alunos e famílias e pela qualidade do seu ambiente interno e das suas relações
      externas.
      Como <b>Missão</b> assumimos o desígnio de “prestar à comunidade um serviço educativo de qualidade, garantindo
      um
      ambiente participativo, aberto e integrador, numa Escola reconhecida pelo seu Humanismo e por elevados padrões
      de
      exigência e responsabilidade, valorizando o prosseguimento de estudos e a integração no mundo do trabalho”.
    </p>
