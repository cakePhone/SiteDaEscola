  <section class="prose">
    <h2>Conselho Pedagógico</h2>
    <p>
      O Conselho Pedagógico é o órgão de coordenação e supervisão pedagógica e orientação educativa da escola,
      nomeadamente nos domínios pedagógico-didático, da orientação e acompanhamento dos alunos e da formação inicial e
      contínua do pessoal docente e não docente.<br>
      A Diretora é, por inerência, presidente do Conselho Pedagógico.
    </p>
