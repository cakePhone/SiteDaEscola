    <h1>Estrutura e Organização Pedagógica e Administrativa</h1>
    <h2>Conselho Geral</h2>
    <p>
      O Conselho Geral é o órgão de direção estratégica, responsável pela definição das linhas orientadoras da
      atividade da Escola, assegurando a participação e representação da comunidade educativa.
    </p>
    <p>
      É composto por sete representantes do pessoal docente; dois representantes do pessoal não docente; quatro
      representantes dos pais e encarregados de educação; dois representantes dos alunos, do Ensino Secundário; três
      representantes do município; três representantes da comunidade local, designadamente de instituições, organizações
      e atividades de caráter económico,social, cultural e científico; pela Diretora que participa nas reuniões do
      Conselho Geral, sem direito a voto.
    </p>
