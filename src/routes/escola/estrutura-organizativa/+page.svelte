    <h2>Diretora</h2>
    <p>
      A Diretora é o órgão de administração e gestão da escola nas áreas pedagógicas, cultural, administrativa,
      financeira e patrimonial, sendo coadjuvado no exercício das suas funções por um Subdiretor e por dois Adjuntos,
      nos termos legais.
    </p>
