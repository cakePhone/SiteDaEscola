    <h1>Selo de Conformidade EQAVET</h1>
    <p>
      A Agência Nacional para a Qualificação e Ensino Profissional (ANQEP) atribuiu à Escola Secundária de Amarante, por
      três anos, o Selo de Conformidade EQAVET.
    </p>
    <img src="/universo-esa/galaxias/eqavet/selo.png" alt="Selo De Conformidade EQAVET">
    <p>
      É com todo o prazer que a direção da Escola Secundária de Amarante comunica a toda a comunidade educativa que,
      pelo terceiro ano consecutivo, foi atribuído a este estabelecimento de ensino o Selo de Qualidade EQAVET, o que
      muito orgulha a comunidade escolar, sobretudo num momento em que o sistema educativo atravessa uma das suas
      notórias crises existenciais.
    </p>
    <p>
      O EQAVET (European Quality Assurance Reference Framework for Vocational Education and Training, ou, em português,
      Quadro de Referência Europeu de Garantia da Qualidade para a Educação e Formação Profissional) foi estabelecido,
      num primeiro momento, pela Recomendação do Parlamento Europeu e do Conselho, de 18 de junho de 2009, e, num
      segundo momento, pela Recomendação do Conselho, de 24 de novembro de 2020, sobre o ensino e a formação
      profissionais, em prol da competitividade sustentável, da justiça social e da resiliência. E configura o
      instrumento de referência para promover e monitorizar o aperfeiçoamento dos sistemas europeus de ensino e formação
      profissional (EFP).
    </p>
    <p>
      Salientando o papel da cultura da melhoria da qualidade e da responsabilidade ao nível dos sistemas e dos
      operadores EFP, atribui um papel determinante à autoavaliação sistemática e compreende mecanismos de avaliação
      interna e de avaliação externa, definidos pelos Estados Membros da União Europeia.
    </p>
    <p>
      A atribuição do EQAVET representa o reconhecimento do nosso trabalho numa região historicamente bem colocada, mas
      que sofre das condições provocadas pelas assimetrias regionais e sociais. E resulta de um trabalho rigoroso de
      análise de uma prestigiada instituição nacional, que trabalha em articulação com as instituições europeias.
    </p>
    <p>
      Com efeito, compete à ANQEP,I.P. (Agência Nacional para a Qualificação e o Ensino Profissional, Instituto Público)
      promover, acompanhar e apoiar a implementação dos sistemas de garantia da qualidade dos processos formativos e dos
      resultados obtidos pelos alunos do ensino profissional, e certificá-los como sistemas EQAVET. E é responsável pela
      definição das orientações técnico-metodológicas de apoio, quer ao processo de alinhamento dos sistemas de garantia
      da qualidade a implementar pelos operadores de EFP, quer à verificação de conformidade dos sistemas de garantia da
      qualidade implementados com o Quadro EQAVET. A verificação de conformidade EQAVET é assegurada por peritos que
      integram a bolsa de peritos externos constituída e gerida pela ANQEP, I.P. Os peritos externos são docentes ou
      investigadores do ensino superior com reconhecida experiência no domínio da gestão e/ou avaliação de sistemas de
      garantia da qualidade, preferencialmente com conhecimentos no domínio do ensino profissional. E são indicados
      pelas instituições de ensino superior que estabeleceram um protocolo de colaboração com a ANQEP,I.P., no âmbito da
      implementação de sistemas de garantia da qualidade na educação e formação profissional, em alinhamento com o
      Quadro EQAVET.
    </p>
    <p>
      Da nossa parte, não regateamos esforços em prol da eficiência e da eficácia do ensino que ministramos, numa linha
      de racionalidade e de postura eminentemente humana. E, como é óbvio em profissionais da educação, fazemos da ação
      pedagógica a prioridade, porém, sempre com base em sólidos parâmeros científicos e tecnológicos e com o recurso a
      bons métodos e técnicas.
    </p>
    <p>
      Em suma, a Escola Secundária de Amarante, quando muitos observadores desferem verrinosas críticas contra o
      trabalho nas escolas, tem passado nos diversos crivos do controlo da qualidade, nomeadamente no que diz respeito
      ao ensino profissional, ainda considerado o parente pobre do sistema por alguns setores da sociedade.
    </p>
    <p>
      Não obstante, a par da excecional dedicação dos professores e formadores, dos alunos, da direção e dos órgãos de
      gestão intermédia, é de relevar o importante contributo da comunidade educativa e das entidades com as quais esta
      escola interage em regime de parceria. A todos o nosso reconhecimento!
    </p>
    <p>Amarante, 22 de março de 2023</p>
    <p class="justify-self-center">A Diretora da Escola Secundária de Amarante.</p>
